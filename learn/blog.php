<h3>Celestial Pearl Danio</h3>
<p class="blogpost-text">The Celestial Pearl Danio, or as its popular internet acronym, CPD,
can be found in the crystal clear rivers of Hopong, Myanmar. This species
is very secluded and its distribution beyond Myanmar is sparse, but has
been found in the waters of the Thanlwin River. Luckily for us hobbyists,
the conditions and overall habitat that these fish dwell are very similar
to typical planted aquariums. Unlike the rivers and streams we may be
accustomed to void of any vegetation, the rivers that support these fish
are brimming with dense mats of local flora. Since the rivers that these
danios come from are constantly moving and are susceptible to changes like
run off and rain, these precious fish are also very hardy once properly
acclimated to their new homes.
</p><br>
<img src="../img/learn/blogpost-1.jpg" alt="Fish">
<p class="blogpost-text">In their typical homes, Celestial Pearl Danios companions include peaceful
community fish of similar sizes such as other Micro Rasboras. Being placid
and generally non-confrontational, these fish do best with other gentle fish.
They love to shoal and the bigger the group you have, the more of this activity
you will see and the more confident these fish are. Make sure to provide
these fish with plenty of cover with plants, driftwood, or rocks.
</p><br>
